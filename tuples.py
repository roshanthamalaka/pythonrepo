# Tuple Data type usage 
# Similar to list but We can change the element of the tuple 

tuple1= (1,2,3)

tuple2= 1,2,3

age= 99
tuple3 = (1,"Hora",age,(7,8)) # Tuple can contain multiple data types

# We can create Tuple with single element
# But make sure to put , at the end

tuple4= (1,)
tuple5 = 8,

print("Tuple Three contains multiple values")
print(tuple3)

print("Tuple 4 Contains single element")

print(tuple4)
